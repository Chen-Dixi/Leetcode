/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    TreeNode* mirrorTree(TreeNode* root) {

        if(root==NULL){
            return;
        }

        mirrorTree(root->left);
        mirrorTree(root->right);

        TreeNode* tmp = root->left;
        root->left = root->right;
        root->right = tmp;
    }
};